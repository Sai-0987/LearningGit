package CoreJavaPackage;

public class PatternPrinting {
	public static void main(String[] args) {
		for (int i = 1; i <= 5; i++) {
			for (int j =4- i; j <=i; j++) {
				System.out.print("* ");
			}
			for(int k=1;k<=1;k++) {
				System.out.println("*");
			}
				
		}
		//System.out.println("");
	}

}
